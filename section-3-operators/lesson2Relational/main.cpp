#include <iostream>

using namespace std;

int main()
{
    int a = 10;
    int b = 5;

    cout << (a == b) << endl; //true (1) when both variables have the same value.
    cout << (a != b) << endl; //true (1) when both variables do NOT have the same value.
    cout << (a > b) << endl; //true (1) when variable a is larger than variable b
    cout << (a < b) << endl; //true (1) when variable a is less than variable b
    cout << (a >= b) << endl; //true (1) when variable a is larger or equal to variable b
    cout << (a <= b) << endl; //true (1) when variable a is less or equal to variable b
}

